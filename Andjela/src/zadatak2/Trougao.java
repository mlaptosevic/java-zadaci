package zadatak2;

public class Trougao {
	
	private Tacka a;
	private Tacka b;
	private Tacka c;
	
	
	public Trougao(Tacka a, Tacka b, Tacka c) {	
		this.a = a;
		this.b = b;
		this.c = c;
	}


	public Tacka getA() {
		return a;
	}


	public Tacka getB() {
		return b;
	}


	public Tacka getC() {
		return c;
	}


	
	
	

}
